package table

import (
	"testing"

	"github.com/admpub/go-pretty/v6/text"
)

func TestTable_Render_BiDiText(t *testing.T) {
	table := Table{}
	table.AppendHeader(Row{"ЧӘЧҗЧЁЧҷЧҡ", "ЧЎЧӣЧ•Чқ", "ЧһЧ—ЧңЧ§Ч”", "ЧӘЧ’ЧҷЧқ"})
	table.AppendRow(Row{"2020-01-01", 5.0, "ЧһЧ—ЧңЧ§Ч”1", []string{"ЧӘЧ’1", "ЧӘЧ’2"}})
	table.AppendRow(Row{"2021-02-01", 5.0, "ЧһЧ—ЧңЧ§Ч”1", []string{"ЧӘЧ’1"}})
	table.AppendRow(Row{"2022-03-01", 5.0, "ЧһЧ—ЧңЧ§Ч”2", []string{"ЧӘЧ’1"}})
	table.AppendFooter(Row{"ЧЎЧ”Чӣ", 30})
	table.SetAutoIndex(true)

	//table.Style().Format.Direction = text.Default
	compareOutput(t, table.Render(), `
+---+------------+------+--------+-----------+
|   | ЧӘЧҗЧЁЧҷЧҡ      | ЧЎЧӣЧ•Чқ | ЧһЧ—ЧңЧ§Ч”  | ЧӘЧ’ЧҷЧқ      |
+---+------------+------+--------+-----------+
| 1 | 2020-01-01 |    5 | ЧһЧ—ЧңЧ§Ч”1 | [ЧӘЧ’1 ЧӘЧ’2] |
| 2 | 2021-02-01 |    5 | ЧһЧ—ЧңЧ§Ч”1 | [ЧӘЧ’1]     |
| 3 | 2022-03-01 |    5 | ЧһЧ—ЧңЧ§Ч”2 | [ЧӘЧ’1]     |
+---+------------+------+--------+-----------+
|   | ЧЎЧ”Чӣ        |   30 |        |           |
+---+------------+------+--------+-----------+`)

	table.Style().Format.Direction = text.LeftToRight
	compareOutput(t, table.Render(), `
вҖӘ+---+------------+------+--------+-----------+
вҖӘ|   | вҖӘЧӘЧҗЧЁЧҷЧҡ      | вҖӘЧЎЧӣЧ•Чқ | вҖӘЧһЧ—ЧңЧ§Ч”  | вҖӘЧӘЧ’ЧҷЧқ      |
вҖӘ+---+------------+------+--------+-----------+
вҖӘ| 1 | вҖӘ2020-01-01 |    вҖӘ5 | вҖӘЧһЧ—ЧңЧ§Ч”1 | вҖӘ[ЧӘЧ’1 ЧӘЧ’2] |
вҖӘ| 2 | вҖӘ2021-02-01 |    вҖӘ5 | вҖӘЧһЧ—ЧңЧ§Ч”1 | вҖӘ[ЧӘЧ’1]     |
вҖӘ| 3 | вҖӘ2022-03-01 |    вҖӘ5 | вҖӘЧһЧ—ЧңЧ§Ч”2 | вҖӘ[ЧӘЧ’1]     |
вҖӘ+---+------------+------+--------+-----------+
вҖӘ|   | вҖӘЧЎЧ”Чӣ        |   вҖӘ30 |        |           |
вҖӘ+---+------------+------+--------+-----------+`)

	table.Style().Format.Direction = text.RightToLeft
	compareOutput(t, table.Render(), `
вҖ«+---+------------+------+--------+-----------+
вҖ«|   | вҖ«ЧӘЧҗЧЁЧҷЧҡ      | вҖ«ЧЎЧӣЧ•Чқ | вҖ«ЧһЧ—ЧңЧ§Ч”  | вҖ«ЧӘЧ’ЧҷЧқ      |
вҖ«+---+------------+------+--------+-----------+
вҖ«| 1 | вҖ«2020-01-01 |    вҖ«5 | вҖ«ЧһЧ—ЧңЧ§Ч”1 | вҖ«[ЧӘЧ’1 ЧӘЧ’2] |
вҖ«| 2 | вҖ«2021-02-01 |    вҖ«5 | вҖ«ЧһЧ—ЧңЧ§Ч”1 | вҖ«[ЧӘЧ’1]     |
вҖ«| 3 | вҖ«2022-03-01 |    вҖ«5 | вҖ«ЧһЧ—ЧңЧ§Ч”2 | вҖ«[ЧӘЧ’1]     |
вҖ«+---+------------+------+--------+-----------+
вҖ«|   | вҖ«ЧЎЧ”Чӣ        |   вҖ«30 |        |           |
вҖ«+---+------------+------+--------+-----------+`)
}
